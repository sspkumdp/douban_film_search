package com.pku.bean;

public class Film {
	private String film_id;
	private String film_name;
	private String director;
	private String screenwriter;
	private String mainactors;
	private String film_type;
	private String area;
	private String lang;
	private String film_date;
	private String film_time;
	private String film_alias;
	private String imdb;
	private String score;
	private String film_summary;
	public String getFilm_id() {
		return film_id;
	}
	public void setFilm_id(String film_id) {
		this.film_id = film_id;
	}
	public String getFilm_name() {
		return film_name;
	}
	public void setFilm_name(String film_name) {
		this.film_name = film_name;
	}
	public String getDirector() {
		return director;
	}
	public void setDirector(String director) {
		this.director = director;
	}
	public String getScreenwriter() {
		return screenwriter;
	}
	public void setScreenwriter(String screenwriter) {
		this.screenwriter = screenwriter;
	}
	public String getMainactors() {
		return mainactors;
	}
	public void setMainactors(String mainactors) {
		this.mainactors = mainactors;
	}
	public String getFilm_type() {
		return film_type;
	}
	public void setFilm_type(String film_type) {
		this.film_type = film_type;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getLang() {
		return lang;
	}
	public void setLang(String lang) {
		this.lang = lang;
	}
	public String getFilm_date() {
		return film_date;
	}
	public void setFilm_date(String film_date) {
		this.film_date = film_date;
	}
	public String getFilm_time() {
		return film_time;
	}
	public void setFilm_time(String film_time) {
		this.film_time = film_time;
	}
	public String getFilm_alias() {
		return film_alias;
	}
	public void setFilm_alias(String film_alias) {
		this.film_alias = film_alias;
	}
	public String getImdb() {
		return imdb;
	}
	public void setImdb(String imdb) {
		this.imdb = imdb;
	}
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
	public String getFilm_summary() {
		return film_summary;
	}
	public void setFilm_summary(String film_summary) {
		this.film_summary = film_summary;
	}
	
}
